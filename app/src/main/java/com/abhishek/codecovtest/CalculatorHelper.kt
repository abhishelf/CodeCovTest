package com.abhishek.codecovtest

object CalculatorHelper {

    fun addUnitTest(num1: Int, num2: Int): Int {
        return num1 + num2
    }

    fun multiplyUnitTest(num1: Int, num2: Int): Int {
        return num1 * num2
    }
        
    fun dividerUnitTest(num1: Int, num2: Int): Int {
        return num1 / num2
    }
    fun subtractUnitTest(num1: Int, num2: Int): Int {
        return num1 - num2
    }

    fun addAndroidTest(num1: Int, num2: Int): Int {
        return num1 + num2
    }

    fun subtractAndroidTest(num1: Int, num2: Int): Int {
        return num1 - num2
    }
    
    fun multiplyAndroidTest(num1: Int, num2: Int): Int {
        return num1 * num2
    }
    
    fun dividerAndroidTest(num1: Int, num2: Int): Int {
        return num1 / num2
    }
}
